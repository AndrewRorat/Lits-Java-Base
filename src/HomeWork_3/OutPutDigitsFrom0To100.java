package HomeWork_3;

    /*
    Вивести суму всіх чисел від 0 до 100
    */

public class OutPutDigitsFrom0To100 {
    public static void main(String[] args) {
        var numbers = -1;
        for (var i = 0; i <= 100; i++) {
            numbers++;
            System.out.println(numbers);
        }
    }
}
